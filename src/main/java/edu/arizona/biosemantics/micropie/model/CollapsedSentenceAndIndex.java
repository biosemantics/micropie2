package edu.arizona.biosemantics.micropie.model;

public class CollapsedSentenceAndIndex {
	
	private String collapsedSentence;
	private String collapsedSentenceIndex;
		
	public String getCollapsedSentence() {
		return collapsedSentence;
	}
	
	public void setCollapsedSentence(String collapsedSentence) {
		this.collapsedSentence = collapsedSentence;
	}

	public String getCollapsedSentenceIndex() {
		return collapsedSentenceIndex;
	}
	
	public void setCollapsedSentenceIndex(String collapsedSentenceIndex) {
		this.collapsedSentenceIndex = collapsedSentenceIndex;
	}	
	
}
